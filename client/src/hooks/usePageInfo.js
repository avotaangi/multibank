// Функция для получения информации о странице по пути
export const getPageInfo = (pathname) => {
  // Проверяем точное совпадение
  if (pageInfo[pathname]) {
    return pageInfo[pathname];
  }

  // Проверяем паттерны с параметрами (например, /card-analytics/:cardId)
  for (const [pattern, info] of Object.entries(pageInfo)) {
    if (pattern.includes(':')) {
      const patternRegex = new RegExp('^' + pattern.replace(/:[^/]+/g, '[^/]+') + '$');
      if (patternRegex.test(pathname)) {
        return info;
      }
    }
  }

  // Возвращаем информацию по умолчанию
  return {
    title: 'Информация',
    color: '#3C82F6', // Синий по умолчанию
    content: ['Информация о странице будет добавлена позже']
  };
};

// Хук для использования информации о странице
export const usePageInfo = () => {
  const location = useLocation();
  return getPageInfo(location.pathname);
};

